import java.util.*;
import java.lang.*;
import java.io.*;
 
 
class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner userInput = new Scanner(System.in);
	    int t;
	    t= userInput.nextInt();
	    while(t!=0){
	        int n=userInput.nextInt();
	        System.out.println(n/2 + 1);
	        --t;
	    }
	}
}
 